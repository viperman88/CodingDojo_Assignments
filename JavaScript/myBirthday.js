var daysUntilMyBirthday = 60;

for(var i = daysUntilMyBirthday; i >= 1; i--){
  if(i >= 30){
    console.log(i + " days until my birthday!! Booooo!!");
  }
  else if(i < 30 && i > 5){
    console.log(i + " days until my birthday!! I'm getting pumped!!");
  }
  else{
    console.log(i + (" days until my birthday"));
  }
}
console.log("IT'S MY BIRTHDAY!!!!!!!!!!!!!!!!!!!!!")
